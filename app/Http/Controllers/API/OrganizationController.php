     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'description'=>'required|unique:orgs'
        ]);

        $org = Organization::create([
            'description'=>$request->description,
            'user_id'=>Auth::id()
        ]);
        return response()->json($org, 200);
    }

